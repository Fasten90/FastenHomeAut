/**
 * \brief	Print IP to string
 */
uint8_t Network_PrintIp(char *str, Network_IP_t *IP)
{
	return usprintf(str, "%d.%d.%d.%d",
			IP->IP[0],
			IP->IP[1],
			IP->IP[2],
			IP->IP[3]);
}



/**
 * \brief	Print IP to string
 */
uint8_t Network_PrintIpOnDebug(char *str, Network_IP_t *IP)
{
	return uprintf("%s IP address: %d.%d.%d.%d\r\n",
			str,
			IP->IP[0],
			IP->IP[1],
			IP->IP[2],
			IP->IP[3]);
}


